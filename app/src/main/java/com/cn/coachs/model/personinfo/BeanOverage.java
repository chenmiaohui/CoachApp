package com.cn.coachs.model.personinfo;


public class BeanOverage {
    private String consumptionName;
    private String consumptionTime;
    private String overages;
    private String detailedMoney;

    public String getConsumptionName() {
        return consumptionName;
    }

    public void setConsumptionName(String consumptionName) {
        this.consumptionName = consumptionName;
    }

    public String getConsumptionTime() {
        return consumptionTime;
    }

    public void setConsumptionTime(String consumptionTime) {
        this.consumptionTime = consumptionTime;
    }

    public String getOverages() {
        return overages;
    }

    public void setOverages(String overages) {
        this.overages = overages;
    }

    public String getDetailedMoney() {
        return detailedMoney;
    }

    public void setDetailedMoney(String detailedMoney) {
        this.detailedMoney = detailedMoney;
    }

}
